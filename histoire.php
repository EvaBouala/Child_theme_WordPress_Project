        <div class =flex-1>
            <h1>Histoire</h1>
            <p>
                Vous incarnez un(e) survivant(e) du crash d'un vaisseau Nautiloïde de mind flayer 
                (flagelleur mental en français) échoué sur sur le continent de Faerûn. Infecté(e) par 
                un parasite illithid (les illithids sont aussi les flagelleurs mentaux) lors de votre enlèvement, 
                vous devez parcourir la Côte des Épées pour trouver un moyen de l'extraire avant qu'il ne vous transforme 
                à votre tour en flagelleur mental. Au fil de l'aventure, vous explorerez forêts enchantées, cités déchues et 
                repaires de cultistes, tout en forgeant des alliances et en affrontant l'Ordre du «Divin» (The Absolute). 
            </p>

            <div class="story-grid">
                <div class="story-act">
                    <h2>Acte 1</h2>
                    <p>
                        La première partie de l'aventure, se déroule sur le continent de Faerûn et inclut le crash du Nautiloïde, 
                        l'exploration des Étendues sauvages, et les rencontres dans le Bosquet d'Émeraude et le Camp des Gobelins. 
                        Cet acte permet au joueur de faire la connaissance de la majorité des compagnons, de se familiariser avec le concept 
                        de jeu D&D et de comprendre l'implication de chaque personnage ou race dans l'histoire. L'acte 1 contient également 
                        plusieurs secrets tels que des objets uniques et des énigmes à résoudre.
                    </p>
                </div>

                <div class="story-act">
                    <h2>Pré-Acte 2 (pour moi ce n'est pas encore l'acte 2)</h2>
                    <p>
                        Une fois l'Acte 1 terminé, le joueur a le choix de rejoindre les Tours Hautelune (Moonrise Towers) et arriver dans 
                        l'Acte 2 soit en passant par le col de la montagne soit en passant par les Tréfonds Obscures (Underdark). Je vous 
                        conseille de faire les deux. Si vous voulez faire les deux dans une seule partie, vous pouvez revenir en arrière avec 
                        la map tout en gardant votre progression. Toutefois, pour passer dans l'Acte 2, les deux ne mènenent pas au même 
                        endroit de la map ni aux mêmes rencontres, ou du moins, pas dans le même ordre. Vous devrez donc choisir de continuer 
                        avec l'un ou l'autre. Dans ce pré-acte, il y aura aussi pleins de choses intéressantes à récupérer dans les deux zones.
                    </p>
                </div>

                <div class="story-act">
                    <h2>Acte 2</h2>
                    <p>
                        Dans cette deuxième partie de l'avanture, vous devrez traverser une zone où la malédiction nécrotique est présente afin 
                        de pouvoir atteindre les Tours Hautelune. Sur le chemin, vous rencontrerez des hordes de monstres sombres et de forces 
                        nécrotiques, Jaheira, et également de vieux amis connus dans l'Acte 1, si vous leur avez parlé. De plus, cet acte dans 
                        les terres maudites est importante pour la prêtresse Ombrecœur et son évolution. Cependant, c'est certes un acte 
                        important tournant principalement autour d'Ombrecœur, mais, il y a toujours des choses à découvrir sur d'autres 
                        compagnons, ce qui vous fera un petit peu avancer dans leur quête. Enfin, comme dans chaque acte, il y a des secrets et 
                        de nouveaux items à découvrir, et je vous conseille vivement de parler au plus de pnj possible pour mieux apprécier 
                        l'histoire, les quêtes et la map, si vous avez du mal avec les choses morbides comme moi (Larian a beaucoup d'humour :3).
                    </p>
                </div>

                <div class="story-act">
                    <h2>Acte 3</h2>
                    <p>
                        La troisième et dernière partie de l'aventure, se déroule principalement à Baldur's Gate, avec des événements critiques 
                        pour le sort des personnages et des quêtes secondaires à accomplir. Cet acte inclut des choix importants et offre des 
                        opportunités pour terminer les quêtes des compagnons. Je vous conseille toujours d'avoir le plus d'interactions possibles 
                        avec les pnj pour d"couvrir le plus de choses (tout particulièrement si vous êtes curieux) et explorez aussi beaucoup. 
                        En tout cas, accrochez-vous (les impatients qui rush), car cette partie est très longue et contient beaucoup de quêtes.
                    </p>
                </div>
            </div>  
        </div>
    </div>
